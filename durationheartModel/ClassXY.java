public class ClassXY {

    float xstartPoint;
    float xendPoint;

    public ClassXY(float xstartPoint, float xendPoint) {
        this.xstartPoint = xstartPoint;
        this.xendPoint = xendPoint;
    }

    public float getXstartPoint() {
        return xstartPoint;
    }

    public void setXstartPoint(int xstartPoint) {
        this.xstartPoint = xstartPoint;
    }

    public float getXendPoint() {
        return xendPoint;
    }

    public void setXendPoint(int xendPoint) {
        this.xendPoint = xendPoint;
    }
}
