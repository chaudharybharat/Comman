public class TextPostionY {

    float y_start;
    float y_end;

    public TextPostionY(float y_start, float y_end) {
        this.y_start = y_start;
        this.y_end = y_end;
    }

    public float getY_start() {
        return y_start;
    }

    public void setY_start(float y_start) {
        this.y_start = y_start;
    }

    public float getY_end() {
        return y_end;
    }

    public void setY_end(float y_end) {
        this.y_end = y_end;
    }
}
